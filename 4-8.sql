CREATE TABLE A
(
A1 CHAR(4),
A2 CHAR(4),
PRIMARY  KEY(A1)
)

CREATE TABLE B
(
B1 CHAR(4),
B2 CHAR(4),
PRIMARY  KEY(B1)
)

CREATE TABLE C
(
C1 CHAR(4),
C2 CHAR(4),
PRIMARY  KEY(C1)
)

CREATE TABLE R1
(
R1_A CHAR(4),
R1_B CHAR(4),
FOREIGN KEY(R1_A) REFERENCES A(A1),
FOREIGN KEY(R1_B) REFERENCES  B(B1)
)

CREATE TABLE R2
(
R2_A CHAR(4),
R2_C CHAR(4),
FOREIGN KEY(R2_C) REFERENCES  C(C1)
)